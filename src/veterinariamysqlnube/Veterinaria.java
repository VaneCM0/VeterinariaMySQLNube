    void listar(){
        String sql = "SELECT * FROM Perros";
        
        try
        {
            cn = con.open();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[]perros = new Object[3];
            modelo = (DefaultTableModel)TablaPerros.getModel();
            
            while(rs.next()){
                perros[0]=rs.getInt("id");
                perros[1]=rs.getString("nombre");
                perros[2]=rs.getString("raza");
                modelo.addRow(perros);
            }
            TablaPerros.setModel(modelo);
        } catch (Exception e)
        {
        }
    }
    
    void agregar(){
        id = Integer.parseInt(txtId.getText());
        String nombre = txtNombre.getText();
        String raza = txtRaza.getText();
        
        if(nombre.equals("")||raza.equals("")){
            JOptionPane.showMessageDialog(null, "Cajas vacias, por favor llenelas");
        } else{
            String sql ="INSERT INTO Perros (id, nombre, raza) VALUES ('"+id+"','"+nombre+"', '"+raza+"')";
            try
            {
                cn = con.open();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Perrito ingresado, GOOF!!!");
                limpiarTabla();
            } catch (Exception e)
            {
                e.printStackTrace();
            }
        }
    }
    
    void eliminar(){
        
        int fila = TablaPerros.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(null, "Perro no seleccionado");
        }else{
            String sql = "DELETE FROM Perros WHERE id ="+id;
            try
            {
                cn = con.open();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Perrito eliminado");
                limpiarTabla();
            } catch (Exception e)
            {
                e.printStackTrace();
            }
        }
        
    }
    
    void limpiarTabla(){
        for (int i = 0; i < TablaPerros.getRowCount(); i++)
        {
            modelo.removeRow(i);
            i=i-1;
        }
    }
    
    void limpiarCajas(){
        txtNombre.setText(null);
        txtRaza.setText(null);
    }

